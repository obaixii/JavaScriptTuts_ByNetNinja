var x = 5

console.log(x < 5) //x is not less than 5 (output - false)
console.log(x <= 5) // x is equals to 5 but not less than 5 (output - true)
console.log(x == 5) // x is equals to 5 (output - true)
console.log(x === "5") // x is equals to 5 but it is not a string (output - false)
console.log(x === 5) // x is equals to 5 and it is a number (output - true)
